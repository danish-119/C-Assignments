#include <stdio.h>

int main()
{
    int num1 = 10, num2 = 5;
    // Addition
    int num3 = num1 + num2;
    printf("%d\n", num3);
    // Subtraction:
    num3 = num1 - num2;
    printf("%d\n", num3);
    // Multiplication:
    num3 = num1 * num2;
    printf("%d\n", num3);
    // Division:
    num3 = num1 / num2;
    printf("%d\n", num3);
    // Remainder:
    num3 = num1 % num2;
    printf("%d", num3);

    return 0;
}